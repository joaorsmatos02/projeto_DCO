package utilizador;

/**
 * Esta classe representa um membro registado no MigrantMatcher, associado a um
 * migrante
 * 
 * @author Joao Matos 56292
 * @author Joao Santos 57103
 *
 */
public class Membro {

	/**
	 * O nome do membro
	 */
	private String nome;

	/**
	 * Construtor da classe
	 * 
	 * @param nome - o nome a associar ao membro
	 */
	public Membro(String nome) {
		this.nome = nome;
	}

}
